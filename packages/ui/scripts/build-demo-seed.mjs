// Build the browser-demo seed from open data (run occasionally, not at runtime).
//
// Fetches World Bank Open Data (CC BY 4.0) for the most-populous ~50 countries plus a few
// development indicators, and bakes each country's flag (flagcdn, public-domain national
// flags) as a data-URI. The result is written to src/lib/demo-seed.ts and committed, so the
// demo bundle ships real data while staying completely network-free at runtime.
//
// Usage: node scripts/build-demo-seed.mjs   (or: npm run seed:demo)
import { writeFile } from 'node:fs/promises';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const COUNT = 50; // most-populous countries to include
const FLAG_WIDTH = 'w160'; // flagcdn raster width (small; baked as data-URI)
const INDICATORS = {
  population: 'SP.POP.TOTL',
  gdp: 'NY.GDP.MKTP.CD',
  gdpPerCapita: 'NY.GDP.PCAP.CD',
  surfaceArea: 'AG.SRF.TOTL.K2',
};

const WB = 'https://api.worldbank.org/v2';

async function wbJson(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`World Bank request failed: ${res.status} ${url}`);
  const body = await res.json();
  if (!Array.isArray(body) || body.length < 2) throw new Error(`Unexpected World Bank shape: ${url}`);
  return body[1] ?? [];
}

/** Most-recent-value indicator map keyed by ISO2 country code. */
async function indicatorByIso2(code) {
  const rows = await wbJson(`${WB}/country/all/indicator/${code}?format=json&mrnev=1&per_page=400`);
  const map = new Map();
  for (const row of rows) {
    const iso2 = row?.country?.id;
    if (iso2 && typeof row.value === 'number') map.set(iso2, { value: row.value, year: row.date });
  }
  return map;
}

async function flagDataUri(iso2) {
  const url = `https://flagcdn.com/${FLAG_WIDTH}/${iso2.toLowerCase()}.png`;
  const res = await fetch(url);
  if (!res.ok) throw new Error(`Flag request failed: ${res.status} ${url}`);
  const buf = Buffer.from(await res.arrayBuffer());
  return `data:image/png;base64,${buf.toString('base64')}`;
}

async function main() {
  console.log('[seed] fetching country list…');
  const rawCountries = await wbJson(`${WB}/country?format=json&per_page=400`);
  // Real countries only (World Bank lists aggregates with region "Aggregates").
  const countries = rawCountries.filter(
    (c) => c?.region?.value && c.region.value !== 'Aggregates' && c.iso2Code,
  );

  console.log('[seed] fetching indicators…');
  const [pop, gdp, gdpPc, area] = await Promise.all([
    indicatorByIso2(INDICATORS.population),
    indicatorByIso2(INDICATORS.gdp),
    indicatorByIso2(INDICATORS.gdpPerCapita),
    indicatorByIso2(INDICATORS.surfaceArea),
  ]);

  const top = countries
    .map((c) => ({ c, population: pop.get(c.iso2Code)?.value ?? 0 }))
    .filter((x) => x.population > 0)
    .sort((a, b) => b.population - a.population)
    .slice(0, COUNT);

  // Distinct regions among the selected countries become parent posts (cross-type relation).
  const regionOrder = [];
  for (const { c } of top) {
    if (!regionOrder.some((r) => r.wbId === c.region.id)) {
      regionOrder.push({ wbId: c.region.id, name: c.region.value });
    }
  }
  const regions = regionOrder.map((r, i) => ({ id: 9001 + i, name: r.name, wbId: r.wbId }));
  const regionPostId = new Map(regions.map((r) => [r.wbId, r.id]));

  console.log(`[seed] baking ${top.length} flags…`);
  const countriesOut = [];
  for (const [i, { c, population }] of top.entries()) {
    const flag = await flagDataUri(c.iso2Code);
    countriesOut.push({
      id: i + 1,
      menuOrder: i + 1,
      name: c.name,
      iso2: c.iso2Code,
      capital: c.capitalCity || '',
      region: c.region.value,
      incomeLevel: c.incomeLevel?.value || '',
      population,
      gdp: gdp.get(c.iso2Code)?.value ?? null,
      gdpPerCapita: gdpPc.get(c.iso2Code)?.value ?? null,
      surfaceArea: area.get(c.iso2Code)?.value ?? null,
      parentRegionId: regionPostId.get(c.region.id) ?? null,
      flag,
    });
  }

  const here = dirname(fileURLToPath(import.meta.url));
  const out = join(here, '..', 'src', 'lib', 'demo-seed.ts');
  const banner = `// AUTO-GENERATED by scripts/build-demo-seed.mjs — do not edit by hand.
// Data: World Bank Open Data (https://data.worldbank.org), licensed CC BY 4.0.
// Flags: flagcdn.com (public-domain national flags), baked as data-URIs.
// Regenerate with: npm run seed:demo
`;
  const body =
    `${banner}\n` +
    `export interface DemoRegionSeed {\n  id: number;\n  name: string;\n}\n\n` +
    `export interface DemoCountrySeed {\n  id: number;\n  menuOrder: number;\n  name: string;\n  iso2: string;\n  capital: string;\n  region: string;\n  incomeLevel: string;\n  population: number;\n  gdp: number | null;\n  gdpPerCapita: number | null;\n  surfaceArea: number | null;\n  parentRegionId: number | null;\n  flag: string;\n}\n\n` +
    `export const DEMO_REGIONS: DemoRegionSeed[] = ${JSON.stringify(
      regions.map((r) => ({ id: r.id, name: r.name })),
      null,
      2,
    )};\n\n` +
    `export const DEMO_COUNTRIES: DemoCountrySeed[] = ${JSON.stringify(countriesOut, null, 2)};\n`;

  await writeFile(out, body, 'utf-8');
  console.log(`[seed] wrote ${out} (${regions.length} regions, ${countriesOut.length} countries)`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
